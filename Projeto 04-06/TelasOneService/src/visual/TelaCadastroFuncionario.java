    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Funcionario func = new Funcionario();
       
        Login login = new Login();
        FuncionariosDAO fD = new FuncionariosDAO();
        LoginDAO lD = new LoginDAO();
     

        if (txtNome.getText().equals("") || txtEmail.getText().equals("") || txtMatricula.getText().equals("") || txtTelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            try {

                func.setNome(txtNome.getText());
                func.setEmail(txtEmail.getText());

                if (!txtConfirmaEmail.getText().equals(txtEmail.getText())) {
                    JOptionPane.showMessageDialog(null, "e-mail invalido");
                 
                     txtConfirmaEmail.setText("");
                     return;
                }

                func.setMatricula(Integer.parseInt(txtMatricula.getText()));
                func.setTelefone(txtTelefone.getText());

                int idFuncionario = fD.insereFuncionario(func);
                func.setIdFuncionario(idFuncionario);
             
                login.setEmail(txtEmail.getText());
                login.setSenha(txtSenha.getText());
                login.setIdFuncionario(idFuncionario);

              
                
                
                 if (!txtConfirmaSenha.getText().equals(txtSenha.getText())) {
                     JOptionPane.showMessageDialog(null, "senha invalida");
                     
                     txtConfirmaSenha.setText("");
                     return;
                 } 
                 
                 lD.insereLogin(login);
                
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
                new TelaFuncionario().setVisible(true);
            
            } catch (Exception e) {
               
                Logger.getLogger(TelaCadastroFuncionario.class.getName()).log(Level.SEVERE, null, e);
             
                this.dispose();

            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

