    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        InserirController iC = new InserirController();
        Login login = new Login();

        if (txtEmail.getText().equals("") || txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            try {

                login = iC.ValidaLogin(txtEmail.getText(), txtSenha.getText());

                String email = txtEmail.getText();
                String senha = txtSenha.getText();

                iC.ValidaLogin(email, senha);

                new TelaFuncionario().setVisible(true);
                this.dispose();

            } catch (Exception e) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, e);

            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

